package com.example.mytestapp

class Mathematics {


    fun addSum(a: Int, b: Int) : Int{
        return (a+b)
    }

    fun divideNumbers(a: Int, b: Int) : Float{
        return (a.toDouble()/b.toDouble()).toFloat()
    }
    fun multiplyNumbers(a: Int, b: Int) : Int{

        return (a*b)
    }

    fun subtractSum(a: Int, b: Int) : Int{

        return (a-b)
    }


}